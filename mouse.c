//使能鼠?
void endble_mouse(struct FIFO32 *p_fifo, int data0, struct MOUSE_DEC *p_mdec)
{
    p_mousefifo = p_fifo;
    mousedata0 = data0;
    
	wait_KBC_sendready();
	io_out8( PORT_KEYCMD, KEYCMD_SENDTO_MOUSE);
	wait_KBC_sendready();
	io_out8( PORT_KEYDAT, MOUSECMD_ENABLE);
	
	p_mdec->phase = 0;  //等待0xfa?段
	return;
	
	
}

int mouse_decode( struct MOUSE_DEC *p_mdec,int dat)
{
	switch(  p_mdec->phase)
	{
		case 0:
			if( dat == 0xfa)
				p_mdec->phase = 1;
			break;
		case 1:
			if( ( dat & 0xc8) == 0x08)
			{
				p_mdec->buf[0] = dat;
				p_mdec->phase ++;
				
			}
			
			break;
		case 2:
			p_mdec->buf[1] = dat;
			p_mdec->phase ++;
			break;
		case 3:
			p_mdec->buf[2] = dat;
			p_mdec->phase = 1;
			p_mdec->btn = p_mdec->buf[0] & 0x07;
			p_mdec->x = p_mdec->buf[1] ;
			p_mdec->y = p_mdec->buf[2] ;
			
			if( ( p_mdec->buf[0] & 0x10) != 0) 
			{
				p_mdec->x |= 0xffffff00;
				
			}
			if( ( p_mdec->buf[0] & 0x20) != 0) 
			{
				p_mdec->y |= 0xffffff00;
				
			}
			
			p_mdec->y = -p_mdec->y;		//鼠?的y方向与画面符号相反
			return 1;
			
			
		default:
			return -1;
		
		
	}
	
	return 0;
}





void inthandler2c( int *esp)
{
	int data;
    
	io_out8( PIC1_OCW2, 0x64);		//PIC1 IRQ 12中断处理完成
	io_out8( PIC0_OCW2, 0x62);		//PIC0 IRQ 2中断处理完成
    
    data = io_in8(PORT_KEYDAT);
    // data += mousedata0;
	
	fifo32_put( p_mousefifo, data + mousedata0);
	
	return;
}
